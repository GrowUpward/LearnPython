"""
变量的作用：重复使用某个值，用变量可以节省内存空间，方便维护
变量的注意事项：
    1.一个变量只能引用一个数值
    2.命名规范：
        -变量只能由字母、数值、下划线_构成；
        -见名知意
        -驼峰标识 englishName
        -非关键字
        -区分大小写
    3.变量名使用前一定要赋值
"""
# 方式1：变量=值
a=1
print(a)

#方式2：变量1，变量2，变量3=值1，值2，值3.....
a,b=2,3
print(a,b)

#方式3:
a=b=4
print(a,b)

print(a+1)
